  re = 1.9650916259e+00;
  De = 3.4443999852e+00;
  PolyOrder = 15;  
  cPol = [ 1.2139578328e+00, 2.8937892884e-02,  2.6155960137e-01,  1.9687327787e-01, -1.6705623038e+00, ...
          -1.4737255523e+00, 7.3987984578e+00,  6.5224495630e+00, -1.6129331399e+01, -1.3780656450e+01, ...
           1.8297016740e+01, 1.4313650931e+01, -9.3426247271e+00, -5.7224634727e+00,  1.3878324200e+00, -1.1920004873e-03];

  RTemp4 =  r.^4;
  re4    = re.^4;
  y      = (RTemp4-re4) ./ (RTemp4+re4);
  y2     = y   .* y;
  y3     = y2  .* y;
  y4     = y3  .* y;
  y5     = y4  .* y;
  y6     = y5  .* y;
  y7     = y6  .* y;
  y8     = y7  .* y;
  y9     = y8  .* y;
  y10    = y9  .* y;
  y11    = y10 .* y;
  y12    = y11 .* y;
  y13    = y12 .* y;
  y14    = y13 .* y;
   
  poly   = cPol(1)       + ... 
           cPol(2).*y    + ... 
           cPol(3).*y2   + ... 
           cPol(4).*y3   + ... 
           cPol(5).*y4   + ... 
           cPol(6).*y5   + ...
           cPol(7).*y6   + ... 
           cPol(8).*y7   + ... 
           cPol(9).*y8   + ... 
           cPol(10).*y9  + ... 
           cPol(11).*y10 + ...
           cPol(12).*y11 + ...
           cPol(13).*y12 + ...
           cPol(14).*y13 + ...
           cPol(15).*y14;

  dpoly  =      cPol(2)       + ...
            2.0*cPol(3).*y    + ...
            3.0*cPol(4).*y2   + ...
            4.0*cPol(5).*y3   + ...
            5.0*cPol(6).*y4   + ...
            6.0*cPol(7).*y5   + ...
            7.0*cPol(8).*y6   + ...
            8.0*cPol(9).*y7   + ...
            9.0*cPol(10).*y8  + ...
           10.0*cPol(11).*y9  + ...
          11.d0*cPol(12).*y10 + ...
          12.d0*cPol(13).*y11 + ...
          13.d0*cPol(14).*y12 + ...
          14.d0*cPol(15).*y13;

  V      =         De .* (1.d0 - exp(-poly .* (r-re))).^2  - De + cPol(16);
  dV     = 2.d0 .* De .* (1.d0 - exp(-poly .* (r-re))) .* (-exp(-poly  .* (r-re))) .* (-dpoly .* (r-re) - poly);

%   V       = V  ;%./27.2113839712790;
%   dV      = dV ;%./27.2113839712790;

end